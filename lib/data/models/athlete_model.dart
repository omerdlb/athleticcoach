class AthleteModel {
  final String id;
  final String name;
  final String surname;
  final DateTime birthDate;
  final String gender;
  final double weight;
  final double height;
  final String branch;

  AthleteModel({
    required this.id,
    required this.name,
    required this.surname,
    required this.birthDate,
    required this.gender,
    required this.weight,
    required this.height,
    required this.branch,
  });
} 